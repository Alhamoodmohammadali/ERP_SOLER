            }

            var claimHelper = new ClaimHelper(User);

            var (userId, userError) = claimHelper.GetUserId();
            if (userError != null)
            {
                return Unauthorized(userError);
            }

            var (tenantId, tenantError) = claimHelper.GetTenantId();
            if (tenantError != null)
            {
                return Unauthorized(tenantError);
            }

            try
            {
                MaintenanceOperation mappedMaintenanceOperation = _mapper.Map<MaintenanceOperation>(MaintenanceOperation);
                mappedMaintenanceOperation.MaintenanceOperationID = id;
                mappedMaintenanceOperation.UserId = userId;
                mappedMaintenanceOperation.TenantID = tenantId;

                await _serviceAggregator.MaintenanceOperationService.Save(mappedMaintenanceOperation, id);

